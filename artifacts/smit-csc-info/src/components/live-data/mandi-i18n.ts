// Suffixes commonly appended to APMC market names. Stripped during
// best-effort lookup in trMarket.
const MARKET_SUFFIXES = [" APMC", " Mandi", " Yard", " Market", "(F&V)", " (F&V)"];

const APMC_LABEL: Record<MandiLang, string> = {
  en: " APMC",
  hi: " एपीएमसी",
  gu: " એપીએમસી",
};

export function trState(s: string, lang: MandiLang): string {
  if (lang === "en" || !s) return s;
  return STATE_TR[s.trim()]?.[lang] ?? s;
}

export function trDistrict(d: string, lang: MandiLang): string {
  if (lang === "en" || !d) return d;
  return DISTRICT_TR[d.trim()]?.[lang] ?? d;
}

export function trCommodity(c: string, lang: MandiLang): string {
  if (lang === "en" || !c) return c;
  const exact = COMMODITY_TR[c]?.[lang];
  if (exact) return exact;
  const trimmed = c.trim();
  return COMMODITY_TR[trimmed]?.[lang] ?? c;
}

export function trVariety(v: string, lang: MandiLang): string {
  if (lang === "en" || !v) return v;
  return VARIETY_TR[v.trim()]?.[lang] ?? v;
}

/**
 * Best-effort market translation. Most APMC market names are
 * "<Town> APMC" — we try exact lookup first, then strip the suffix and
 * translate the prefix via DISTRICT_TR (towns are reused there). Falls
 * back to the original English string when nothing matches.
 */
export function trMarket(m: string, lang: MandiLang): string {
  if (lang === "en" || !m) return m;
  const exact = DISTRICT_TR[m.trim()]?.[lang];
  if (exact) return exact;
  for (const suf of MARKET_SUFFIXES) {
    if (m.endsWith(suf)) {
      const prefix = m.slice(0, -suf.length).trim();
      const trPrefix = DISTRICT_TR[prefix]?.[lang];
      if (trPrefix) {
        if (suf === " APMC") return trPrefix + APMC_LABEL[lang];
        return trPrefix + suf;
      }
    }
  }
  return m;
}
